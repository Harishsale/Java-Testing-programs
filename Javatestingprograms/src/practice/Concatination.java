package practice;

public class Concatination {

	public static void main(String[] args) {
		String str="Base";
		int a=123;
		System.out.println(str+ "ball");
		System.out.println(str+a);
		
		String str1="Volley";
		int a1=123;
		System.out.println(str1+ "ball");
		System.out.println(str1+a1);
		
		
	}

}
