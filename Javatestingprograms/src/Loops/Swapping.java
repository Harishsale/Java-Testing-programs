package Loops;

public class Swapping {

	public static void main(String[] args)
	{
		int a=10;
		int b=25;
		int temp;
		  
		  temp=a; //temp=10 a=0
		  a=b;     // a=25 b=0
		  b=temp;   //b=10
		  System.out.println("after swapping a= " +a);
		  System.out.println("after swapping b= " +b);


	}

}
